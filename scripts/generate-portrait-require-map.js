#!/usr/bin/env node
const fs = require('fs');
const path = require('path');

// Paths
const repoRoot = path.resolve(__dirname, '..');
const assetsDir = path.join(repoRoot, 'assets', 'images', 'characters');
const outFile = path.join(repoRoot, 'src', 'services', 'portraitRequireMap.ts');

function walkDir(dir) {
  return fs.readdirSync(dir, { withFileTypes: true }).map(d => ({ name: d.name, dirent: d }));
}

function generate() {
  if (!fs.existsSync(assetsDir)) {
    console.error('Assets directory not found:', assetsDir);
    process.exit(1);
  }

  const portraitEntries = [];
  const expressionEntries = {};

  const classes = fs.readdirSync(assetsDir, { withFileTypes: true }).filter(d => d.isDirectory()).map(d => d.name);
  classes.forEach(cls => {
    const clsDir = path.join(assetsDir, cls);
    const childs = fs.readdirSync(clsDir, { withFileTypes: true }).filter(d => d.isDirectory()).map(d => d.name);
    childs.forEach(child => {
      const childDir = path.join(clsDir, child);
      // look for portrait.png or portrait.webp
      const portraitPathCandidates = ['portrait.webp', 'portrait.png', 'portrait.jpg'].map(n => path.join(childDir, n));
      const foundPortrait = portraitPathCandidates.find(p => fs.existsSync(p));
      if (foundPortrait) {
        // Key is expected to be the folder name (child)
        const key = child;
        // require path from src/services => ../../assets/...
        const relRequire = '../../' + path.relative(path.join(repoRoot, 'src', 'services'), foundPortrait).replace(/\\/g, '/');
        portraitEntries.push({ key, requirePath: relRequire });
      }

      // expressions: files under childDir matching expression_<name>.* or in expressions/ subdir
      const exprDir = path.join(childDir, 'expressions');
      if (fs.existsSync(exprDir) && fs.statSync(exprDir).isDirectory()) {
        const exprFiles = fs.readdirSync(exprDir).filter(f => /\.(png|webp|jpg)$/i.test(f));
        if (exprFiles.length > 0) {
          expressionEntries[child] = {};
          exprFiles.forEach(f => {
            const m = f.match(/expression[_-](.+?)\./i);
            const name = m ? m[1] : path.parse(f).name;
            const relRequire = '../../' + path.relative(path.join(repoRoot, 'src', 'services'), path.join(exprDir, f)).replace(/\\/g, '/');
            expressionEntries[child][name] = relRequire;
          });
        }
      } else {
        // fallback: files named expression_<name>.* in childDir
        const exprFiles = fs.readdirSync(childDir).filter(f => /^expression[_-].+\.(png|webp|jpg)$/i.test(f));
        if (exprFiles.length > 0) {
          expressionEntries[child] = {};
          exprFiles.forEach(f => {
            const m = f.match(/^expression[_-](.+?)\./i);
            const name = m ? m[1] : path.parse(f).name;
            const relRequire = '../../' + path.relative(path.join(repoRoot, 'src', 'services'), path.join(childDir, f)).replace(/\\/g, '/');
            expressionEntries[child][name] = relRequire;
          });
        }
      }
    });
  });

  // Build TS output
  let out = `/** THIS FILE IS AUTO-GENERATED BY scripts/generate-portrait-require-map.js */\n\n`;
  out += `export const PORTRAIT_REQUIRE_MAP: Record<string, number> = {\n`;
  portraitEntries.forEach(e => {
    out += `  '${e.key}': require('${e.requirePath}'),\n`;
  });
  out += `};\n\n`;

  out += `export const EXPRESSION_REQUIRE_MAP: Record<string, Record<string, number>> = {\n`;
  Object.keys(expressionEntries).forEach(key => {
    out += `  '${key}': {\n`;
    Object.keys(expressionEntries[key]).forEach(expr => {
      out += `    '${expr}': require('${expressionEntries[key][expr]}'),\n`;
    });
    out += `  },\n`;
  });
  out += `};\n`;

  fs.writeFileSync(outFile, out, 'utf8');
  console.log('Wrote', outFile);
}

generate();
